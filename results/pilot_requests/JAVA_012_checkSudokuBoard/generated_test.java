import org.junit.jupiter.api.Test;
import static org.junit.jupiter.api.Assertions.*;

public class CheckSudokuBoardTest {

    @Test
    void testValidSudokuBoard() {
        int[][] validBoard = {
            {5, 3, 0, 0, 7, 0, 0, 0, 0},
            {6, 0, 0, 1, 9, 5, 0, 0, 0},
            {0, 9, 8, 0, 0, 0, 0, 6, 0},
            {8, 0, 0, 0, 6, 0, 0, 0, 3},
            {4, 0, 0, 8, 0, 3, 0, 0, 1},
            {7, 0, 0, 0, 2, 0, 0, 0, 6},
            {0, 6, 0, 0, 0, 0, 2, 8, 0},
            {0, 0, 0, 4, 1, 9, 0, 0, 5},
            {0, 0, 0, 0, 8, 0, 0, 7, 9}
        };
        assertTrue(JAVA_012_checkSudokuBoard(validBoard));
    }

    @Test
    void testNullBoard() {
        assertFalse(JAVA_012_checkSudokuBoard(null));
    }

    @Test
    void testBoardWithIncorrectDimensions() {
        int[][] tooSmallBoard = {
            {1, 2, 3},
            {4, 5, 6},
            {7, 8, 9}
        };
        assertFalse(JAVA_012_checkSudokuBoard(tooSmallBoard));

        int[][] tooLargeBoard = new int[10][10];
        assertFalse(JAVA_012_checkSudokuBoard(tooLargeBoard));
    }

    @Test
    void testBoardWithInvalidValues() {
        int[][] boardWithNegative = {
            {5, 3, -1, 0, 7, 0, 0, 0, 0},
            {6, 0, 0, 1, 9, 5, 0, 0, 0},
            {0, 9, 8, 0, 0, 0, 0, 6, 0},
            {8, 0, 0, 0, 6, 0, 0, 0, 3},
            {4, 0, 0, 8, 0, 3, 0, 0, 1},
            {7, 0, 0, 0, 2, 0, 0, 0, 6},
            {0, 6, 0, 0, 0, 0, 2, 8, 0},
            {0, 0, 0, 4, 1, 9, 0, 0, 5},
            {0, 0, 0, 0, 8, 0, 0, 7, 9}
        };
        assertFalse(JAVA_012_checkSudokuBoard(boardWithNegative));

        int[][] boardWithTooLargeValue = {
            {5, 3, 10, 0, 7, 0, 0, 0, 0},
            {6, 0, 0, 1, 9, 5, 0, 0, 0},
            {0, 9, 8, 0, 0, 0, 0, 6, 0},
            {8, 0, 0, 0, 6, 0, 0, 0, 3},
            {4, 0, 0, 8, 0, 3, 0, 0, 1},
            {7, 0, 0, 0, 2, 0, 0, 0, 6},
            {0, 6, 0, 0, 0, 0, 2, 8, 0},
            {0, 0, 0, 4, 1, 9, 0, 0, 5},
            {0, 0, 0, 0, 8, 0, 0, 7, 9}
        };
        assertFalse(JAVA_012_checkSudokuBoard(boardWithTooLargeValue));
    }

    @Test
    void testBoardWithDuplicateValuesInRow() {
        int[][] boardWithRowDuplicates = {
            {5, 3, 5, 0, 7, 0, 0, 0, 0},
            {6, 0, 0, 1, 9, 5, 0, 0, 0},
            {0, 9, 8, 0, 0, 0, 0, 6, 0},
            {8, 0, 0, 0, 6, 0, 0, 0, 3},
            {4, 0, 0, 8, 0, 3, 0, 0, 1},
            {7, 0, 0, 0, 2, 0, 0, 0, 6},
            {0, 6, 0, 0, 0, 0, 2, 8, 0},
            {0, 0, 0, 4, 1, 9, 0, 0, 5},
            {0, 0, 0, 0, 8, 0, 0, 7, 9}
        };
        assertFalse(JAVA_012_checkSudokuBoard(boardWithRowDuplicates));
    }

    @Test
    void testBoardWithDuplicateValuesInColumn() {
        int[][] boardWithColumnDuplicates = {
            {5, 3, 0, 0, 7, 0, 0, 0, 0},
            {6, 0, 0, 1, 9, 5, 0, 0, 0},
            {5, 9, 8, 0, 0, 0, 0, 6, 0},
            {8, 0, 0, 0, 6, 0, 0, 0, 3},
            {4, 0, 0, 8, 0, 3, 0, 0, 1},
            {7, 0, 0, 0, 2, 0, 0, 0, 6},
            {0, 6, 0, 0, 0, 0, 2, 8, 0},
            {0, 0, 0, 4, 1, 9, 0, 0, 5},
            {0, 0, 0, 0, 8, 0, 0, 7, 9}
        };
        assertFalse(JAVA_012_checkSudokuBoard(boardWithColumnDuplicates));
    }

    @Test
    void testEmptyBoard() {
        int[][] emptyBoard = new int[9][9];
        assertTrue(JAVA_012_checkSudokuBoard(emptyBoard));
    }
}